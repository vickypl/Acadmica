public class Assignment {

	private String id;
	private String course;
	private String regid;
	private String assingment;
	private String assingedBy;
	private String assDate;
	private String subDate;
	private String status;  //checked unchecked
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getRegid() {
		return regid;
	}
	public void setRegid(String regid) {
		this.regid = regid;
	}
	public String getAssingment() {
		return assingment;
	}
	public void setAssingment(String assingment) {
		this.assingment = assingment;
	}
	public String getAssingedBy() {
		return assingedBy;
	}
	public void setAssingedBy(String assingedBy) {
		this.assingedBy = assingedBy;
	}
	public String getAssDate() {
		return assDate;
	}
	public void setAssDate(String assDate) {
		this.assDate = assDate;
	}
	public String getSubDate() {
		return subDate;
	}
	public void setSubDate(String subDate) {
		this.subDate = subDate;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}